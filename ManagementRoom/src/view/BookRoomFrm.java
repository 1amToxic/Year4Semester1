    public void setClient(Client client){
        lbBookFullname.setText(client.getNameClient());
        lbBookID.setText(client.getId()+"");
        lbBookAddress.setText(client.getAddress());
        Date date = new Date();
        SimpleDateFormat simpleDateFormat =new SimpleDateFormat(MainSQLStatement.SIMPLE_DATE_FORMAT);
        String s = simpleDateFormat.format(date);
        tfBookTimeStart.setText(s);
        s = simpleDateFormat.format(date);
        tfBookTimeEnd.setText(s);
    }
