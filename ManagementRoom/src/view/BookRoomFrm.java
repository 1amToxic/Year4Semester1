    public Date getTimeStart(){
        Date date = null;
        SimpleDateFormat format = new SimpleDateFormat(MainSQLStatement.SIMPLE_DATE_FORMAT);
        try {
            date =  format.parse(tfBookTimeStart.getText());
            return date;
            
        } catch (ParseException ex) {
            Logger.getLogger(BookRoomFrm.class.getName()).log(Level.SEVERE, null, ex);
        }
        return date;
    }
    public Date getTimeEnd(){
        Date date = new Date();
        SimpleDateFormat format = new SimpleDateFormat(MainSQLStatement.SIMPLE_DATE_FORMAT);
        try {
            date =  format.parse(tfBookTimeEnd.getText());
            return date;
        } catch (ParseException ex) {
            Logger.getLogger(BookRoomFrm.class.getName()).log(Level.SEVERE, null, ex);
        }
        return date;
    }
    public Room getRoom(){
        return new Room( Integer.parseInt(cbbBookRank.getSelectedItem().toString())
                , cbbBookDes.getSelectedItem().toString());
    }
    public void addToTable(ArrayList<Room> listRoom){
        model.setRowCount(0);
        for(Room r : listRoom ){
            model.addRow(r.toObjects());
        }
    }
    public int getSelectedRow(){
        return tableSearchRoom.getSelectedRow();
    }
